.macro POP_REGS
    pop r15
    pop r14
    pop r13
    pop r12
    pop r11
    pop r10
    pop r9
    pop r8
    pop rsi
    pop rdi
    pop rbp
    pop rdx
    pop rcx
    pop rbx
    pop rax
.endm

// Exceptions/IRQs WITHOUT error code: push dummy error, then vector
.macro ISR_NOERR n
.global isr\n
isr\n:
    push 0
    push \n
    jmp isr_common_stub
.endm

// Exceptions WITH error code: CPU already pushed error, we push vector
.macro ISR_ERR n
.global isr\n
isr\n:
    push \n
    jmp isr_common_stub
.endm

.global isr_common_stub
isr_common_stub:
    cli
    PUSH_REGS

    // rdi = pointer to our saved context struct (starts at r15)
    mov rdi, rsp

    // SysV ABI alignment: make RSP 16-byte aligned before call
    sub rsp, 8
    call isr_common_handler
    add rsp, 8

    POP_REGS

    // drop vector + error
    add rsp, 16
    iretq

